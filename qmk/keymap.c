static void oled_sync_handler(uint8_t in_buflen, const void *in_data, uint8_t out_buflen, void *out_data) {
    oled_anim = *(const uint8_t *)in_data;
}

void keyboard_post_init_user(void) {
    transaction_register_rpc(OLED_SYNC, oled_sync_handler);
    rgblight_sethsv_noeeprom(170, 255, 120);
    rgblight_mode_noeeprom(RGBLIGHT_MODE_STATIC_LIGHT);
}

void housekeeping_task_user(void) {
    if (is_keyboard_master()) {
        static uint8_t last = 0xFF;
        if (oled_anim != last) {
            last = oled_anim;
            transaction_rpc_send(OLED_SYNC, sizeof(oled_anim), &oled_anim);
        }
    }
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (keycode == OLED_ANM && record->event.pressed) {
        oled_anim = (oled_anim + 1) % 3;
    }
    return true;
}

